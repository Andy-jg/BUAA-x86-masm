STACK 	SEGMENT	PARA	STACK
		DW		100H DUP(?)
STACK	ENDS

DATA	SEGMENT	PARA
	LEN EQU 5
	X 	DB 	0
	Y 	DB 	0
	Z 	DB 	?
	NL 	DB	13,10,'$'
DATA 	ENDS

CODE 	SEGMENT PARA
		ASSUME	CS:CODE,DS:DATA,SS:STACK
NEWLINE MACRO
		PUSH 	AX
		PUSH 	DX
		MOV 	DX,OFFSET NL
		MOV 	AH,9
		INT 	21H
		POP 	DX
		POP 	AX
		ENDM
GETNUM  PROC
	INPUT:
		MOV 	AH,1
		INT 	21H
		SUB	 	AL,30H
		XOR 	AH,AH
		RET
GETNUM 	ENDP

OUTPUT 	PROC
	PRINT:
		PUSH 	DX
		PUSH 	CX
		PUSH	BX
		PUSH 	AX
		NEWLINE
		MOV 	CX,LEN
		MOV 	BX,10
	PRINT_LP1:
		XOR 	DX,DX
		DIV		BX
		PUSH	DX
		LOOP 	PRINT_LP1

		MOV 	CX,LEN
		MOV 	BX,0
	PRINT_LP2:
		POP 	DX
		CMP 	DL,0
		JNE		PRINT_LP2_1
		CMP		BX,0
		JZ 		PRINT_LP2_2
	PRINT_LP2_1:
		MOV 	BX,1
		MOV 	AH,2
		OR 		DL,30H
		INT 	21H
		
	PRINT_LP2_2:
		LOOP 	PRINT_LP2
		POP 	AX
		POP 	BX
		POP 	CX
		POP 	DX
		RET
OUTPUT 	ENDP


MAIN	PROC 	FAR
	MAINPROC:
		MOV 	AX,DATA
		MOV 	DS,AX
		MOV 	ES,AX

		CALL 	GETNUM
		MOV 	BL,10
		MUL		BL
		MOV		X,AL
		CALL	GETNUM
		ADD		X,AL

		CALL	GETNUM 	
		MOV		BL,10
		MUL		BL
		MOV 	Y,AL
		CALL	GETNUM
		ADD		Y,AL

		MOV 	AL,X
		MOV	 	BL,Y
		MUL 	BL

		CALL 	OUTPUT

	EXIT:	
		MOV 	AX,4C00H
		INT 	21H
MAIN 	ENDP

CODE 	ENDS
		END 	MAIN