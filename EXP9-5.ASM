STACK 	SEGMENT	PARA	STACK
		DW		100H DUP(?)
STACK	ENDS

DATA	SEGMENT	PARA
LEN		EQU 	121
IN_BUF	DB 		LEN-1		;ALLOWED MAX LEN
		DB 		?			;ACTUAL LEN
		DB 		LEN DUP(?)	;INPUT STRING
DATA 	ENDS

CODE 	SEGMENT PARA
		ASSUME	CS:CODE,DS:DATA,SS:STACK
MAIN	PROC 	FAR
		MOV 	AX,DATA
		MOV 	DS,AX

		MOV 	DX,OFFSET IN_BUF
		MOV 	AH,0AH
		INT 	21H

		MOV 	CL,IN_BUF+1			;SET CX TO LEN FOR LOOP
		XOR 	CH,CH
		MOV 	SI,OFFSET IN_BUF+2  ;GET ACTUAL STRING

LP1: 	MOV 	AL,[SI]  			;TO UPPER 
		CMP 	AL,'a'
		JB 		LP2
		CMP 	AL,'z'
		JA 		LP2
		SUB 	AL,20H
		MOV 	[SI],AL

LP2: 	INC 	SI
		LOOP	LP1

		MOV 	BYTE PTR [SI],'$'  	;PUT END TO STRING 

		MOV 	AH,2  	   ;NEWLINE\R\N
		MOV		DL,0DH
		INT   	21H
		MOV 	AH,2
		MOV 	DL,0AH
		INT  	21H

		MOV 	DX,OFFSET IN_BUF+2
		MOV 	AH,09H
		INT 	21H

EXIT:	MOV 	AX,4C00H
		INT 	21H
MAIN 	ENDP

CODE 	ENDS
		END 	MAIN