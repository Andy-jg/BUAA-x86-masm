STACK	SEGMENT	PARA STACK
		DW		100H DUP(?)
STACK	ENDS

DATA	SEGMENT	PARA
X 		DW		6
Y		DW		1
Z		DW 		0
DIVISOR DW 		10
DATA	ENDS

CODE	SEGMENT	PARA
ASSUME	CS:CODE,DS:DATA,SS:STACK

MAIN	PROC	FAR
		MOV 	AX,DATA
		MOV 	DS,AX

		CALL 	CAL

		CALL 	DEC_OUTPUT

EXIT:	MOV		AX,4C00H
		INT		21H
MAIN	ENDP

CAL 	PROC
		MOV 	AX,X
		MOV 	BX,Y
		ADD		AX,BX
		MOV 	BX,8
		MUL 	BX
		MOV 	BX,X
		SUB 	AX,BX
		MOV 	BX,2
		DIV 	BX

		MOV 	Z,AX		
		RET
CAL 	ENDP

DEC_OUTPUT 		PROC
		MOV 	AX,Z
		MOV 	CX,0
LP1:	XOR 	DX,DX
		DIV 	WORD PTR DIVISOR
		OR 		DL,30H
		PUSH 	DX
		INC 	CX
		CMP 	AX,0
		JA 		LP1

LP2: 	POP 	DX
		MOV 	AH,2
		INT 	21H
		LOOP 	LP2
		RET
DEC_OUTPUT 		ENDP

CODE	ENDS
		END 	MAIN